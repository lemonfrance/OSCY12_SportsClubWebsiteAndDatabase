<p> Kiwi Ormiston Athletic Club is one of New Zealand’s newest
 athletic clubs situated in Manukau in South Auckland, New Zealand.
 We aim to develop abilities and skills not only physically and
 mentally but also socially, as we believe that interactive
 learning is an effective tool for success. We aim to turn
 their weaknesses into strengths and bring out the best athlete
 in them through a range of fun but challenging activities.
 We accept club members of ages 6-19 not only from Ormiston but
 also those who have a passion for athletics and are interested
 to join in our club.
